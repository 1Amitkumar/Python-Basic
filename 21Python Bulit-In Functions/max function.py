print(max("abcDEF"))  # find largest item in the string

print(max([2, 1, 4, 3]))  # find largest item in the list

print(max(("one", "two", "three")))  # find largest item in the tuple

print(max({1: "one", 2: "two", 3: "three"}))  # find largest item in the dict

print(max(20, 10, 30, -5))

print(max("c", "b", "a", "Y", "Z"))

print(max(3.14, -9.91, 2.41))
